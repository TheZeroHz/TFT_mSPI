from ..core.panel import Panel


class GC9A01(Panel):
    width = 240
    height = 240

    SLPOUT = 0x11
    DISPON = 0x29
    MADCTL = 0x36
    COLMOD = 0x3A

    TFT_MAD_MY = 0x80
    TFT_MAD_MX = 0x40
    TFT_MAD_MV = 0x20
    TFT_MAD_BGR = 0x08
    TFT_MAD_RGB = 0x00

    pixel_format = 16

    def __init__(self, bus, *, bgr=True, x_offset=0, y_offset=0):
        super().__init__(bus, bgr=bgr, x_offset=x_offset, y_offset=y_offset)

    def init_sequence(self):
        return (
            (0xEF, None, 0),
            (0xEB, bytes([0x14]), 0),
            (0xFE, None, 0),
            (0xEF, None, 0),
            (0xEB, bytes([0x14]), 0),
            (0x84, bytes([0x40]), 0),
            (0x85, bytes([0xFF]), 0),
            (0x86, bytes([0xFF]), 0),
            (0x87, bytes([0xFF]), 0),
            (0x88, bytes([0x0A]), 0),
            (0x89, bytes([0x21]), 0),
            (0x8A, bytes([0x00]), 0),
            (0x8B, bytes([0x80]), 0),
            (0x8C, bytes([0x01]), 0),
            (0x8D, bytes([0x01]), 0),
            (0x8E, bytes([0xFF]), 0),
            (0x8F, bytes([0xFF]), 0),
            (0xB6, bytes([0x00, 0x20]), 0),
            (self.COLMOD, bytes([0x05]), 0),
            (0x90, bytes([0x08, 0x08, 0x08, 0x08]), 0),
            (0xBD, bytes([0x06]), 0),
            (0xBC, bytes([0x00]), 0),
            (0xFF, bytes([0x60, 0x01, 0x04]), 0),
            (0xC3, bytes([0x13]), 0),
            (0xC4, bytes([0x13]), 0),
            (0xC9, bytes([0x22]), 0),
            (0xBE, bytes([0x11]), 0),
            (0xE1, bytes([0x10, 0x0E]), 0),
            (0xDF, bytes([0x21, 0x0C, 0x02]), 0),
            (0xF0, bytes([0x45, 0x09, 0x08, 0x08, 0x26, 0x2A]), 0),
            (0xF1, bytes([0x43, 0x70, 0x72, 0x36, 0x37, 0x6F]), 0),
            (0xF2, bytes([0x45, 0x09, 0x08, 0x08, 0x26, 0x2A]), 0),
            (0xF3, bytes([0x43, 0x70, 0x72, 0x36, 0x37, 0x6F]), 0),
            (0xED, bytes([0x1B, 0x0B]), 0),
            (0xAE, bytes([0x77]), 0),
            (0xCD, bytes([0x63]), 0),
            (0x70, bytes([0x07, 0x07, 0x04, 0x0E, 0x0F, 0x09, 0x07, 0x08, 0x03]), 0),
            (0xE8, bytes([0x34]), 0),
            (0x62, bytes([0x18, 0x0D, 0x71, 0xED, 0x70, 0x70, 0x18, 0x0F, 0x71, 0xEF, 0x70, 0x70]), 0),
            (0x63, bytes([0x18, 0x11, 0x71, 0xF1, 0x70, 0x70, 0x18, 0x13, 0x71, 0xF3, 0x70, 0x70]), 0),
            (0x64, bytes([0x28, 0x29, 0xF1, 0x01, 0xF1, 0x00, 0x07]), 0),
            (0x66, bytes([0x3C, 0x00, 0xCD, 0x67, 0x45, 0x45, 0x10, 0x00, 0x00, 0x00]), 0),
            (0x67, bytes([0x00, 0x3C, 0x00, 0x00, 0x00, 0x01, 0x54, 0x10, 0x32, 0x98]), 0),
            (0x74, bytes([0x10, 0x85, 0x80, 0x00, 0x00, 0x4E, 0x00]), 0),
            (0x98, bytes([0x3E, 0x07]), 0),
            (0x35, None, 0),
            (0x21, None, 0),
            (self.SLPOUT, None, 120),
            (self.DISPON, None, 20),
        )

    def set_rotation(self, tft, rotation):
        rot = int(rotation) & 3
        co = self.TFT_MAD_BGR if self.bgr else self.TFT_MAD_RGB
        if rot == 0:
            mad = co
        elif rot == 1:
            mad = self.TFT_MAD_MX | self.TFT_MAD_MV | co
        elif rot == 2:
            mad = self.TFT_MAD_MX | self.TFT_MAD_MY | co
        else:
            mad = self.TFT_MAD_MV | self.TFT_MAD_MY | co
        self.bus.write_cmd(self.MADCTL)
        self.bus.write_data(bytearray([mad & 0xFF]))

